#include "3-calc.h"

/**
 * op_add - Adds a and b
 * @a: First integer
 * @b: Second Integer
 * Return: Sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - Subtract b from a
 * @a: First integer
 * @b: Second Integer
 * Return: Result of a minus b
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - Multiplies a and b
 * @a: First integer
 * @b: Second Integer
 * Return: Multiples a and b
 */


int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - Divides a by b
 * @a: First integer
 * @b: Second Integer
 * Return: Result of a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - Modulo of a when divided by b
 * @a: First integer
 * @b: Second Integer
 * Return: Modulo of a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
